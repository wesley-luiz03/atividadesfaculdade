#Pedindo o número para saber o sucessor e seu antecessor
number = int(input("Digite um número: "))

#Calculo do antecessor e sucessor
antec_number = (number - 1)
suces_number = (number + 1)

#Imprimindo o antecessor e sucessor do número escolhido
print(f"O antecessor do seu número é {antec_number} e o sucessor do seu número é  {suces_number}")